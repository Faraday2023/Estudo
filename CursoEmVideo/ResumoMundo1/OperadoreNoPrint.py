# Operadores no Print

print('Oi'+'Ola') # Concatenou as duas palavras
print('Oi'*5) # Imprimiu 'oi' 5 vezes
print('{:>20}'.format('Marcos Paulo')) # Imprimiu Marcos Paulo em 20 espaços alinhado a direita
print('{:<20}'.format('Marcos Paulo')) # Imprimiu Marcos Paulo em 20 espaços alinhado a esquerda
print('{:^20}'.format('Marcos Paulo')) # Imprimiu Marcos Paulo centralizado em 20 espaços
print('{:-^20}'.format('Marcos Paulo')) # Imprimiu Marcos Paulo centralizado em 20 espaços com hífens
