# From math import sqrt

import math

a = math.sqrt(4)

print('Utilizando format no final: {:.2f}'.format(a))

b = math.ceil(5/2)

print('Utilizando format no final: {:.2f}'.format(b))

print(f'Utilizando format no começo: {b:.2f}')

c = math.pow(2,3)

print(c)

d = math.floor(3/2)

print(d)

e = math.factorial(3)

print(e)

f = math.trunc(5/2)

print(f)